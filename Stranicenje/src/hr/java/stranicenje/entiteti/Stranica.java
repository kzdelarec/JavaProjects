package hr.java.stranicenje.entiteti;


public class Stranica {
	
	Integer korak;
	Integer ulaz;
	String sto;
	String izlaz;
	String memorija;
	
	public Integer getKorak() {
		return korak;
	}
	public void setKorak(Integer korak) {
		this.korak = korak;
	}
	public Integer getUlaz() {
		return ulaz;
	}
	public void setUlaz(Integer ulaz) {
		this.ulaz = ulaz;
	}
	public String getSto() {
		return sto;
	}
	public void setSto(String sto) {
		this.sto = sto;
	}
	public String getIzlaz() {
		return izlaz;
	}
	public void setIzlaz(String izlaz) {
		this.izlaz = izlaz;
	}
	public String getMemorija() {
		return memorija;
	}
	public void setMemorija(String memorija) {
		this.memorija = memorija;
	}
}
